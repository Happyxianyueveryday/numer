%复合五点高斯公式积分函数 
%参数说明：a为积分下限，b为积分上限，n为分段数
function s=Gause_5(a,b,n)
format long
h=(b-a)/n;       %步长
x=a:h:b;         %x的分段点
f=@(x) 1/(1+x*x);  %被积函数
s=0;
for k=1:n
    s=s+0.2369269*f(h/2*0.9061798+(x(k)+x(k+1))/2)+0.2369269*f(h/2*(-0.9061798)+(x(k)+x(k+1))/2)+0.4786287*f(h/2*0.5384693+(x(k)+x(k+1))/2)+0.4786287*f(h/2*(-0.5384693)+(x(k)+x(k+1))/2)+0.5688889*f((x(k)+x(k+1))/2);   %该公式的推导请直接参见本说明文档上图（*）式
end
s=h/2*s;
end